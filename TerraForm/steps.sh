
## Configurez votre compte AWS en renseignant vos clés d'accès

export AWS_ACCESS_KEY_ID=<your-access-key>
export AWS_SECRET_ACCESS_KEY=<your-secret-access-key>

## Initialisation du projet Terraform

terraform init

## Vérification de la syntaxe du code Terraform

terraform validate

## Vérification de la configuration Terraform

terraform plan

## Création de l'infrastructure

terraform apply

